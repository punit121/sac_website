						The My Imprint Program follows a certain way of functioning. As the My Imprint Program says “By the Students, for the Students”, a My Imprint Class Gift Committee (MICGC) is set up each year which includes the incumbent and ex Vice Presidents, TSG and G-Secs, Students’ Alumni Cell along with the incumbent Hall Presidents of IIT Kharagpur. The MCIGC formulates 9 relevant options which are distributed equally under three slabs. One of the slabs decided by the number of registrations goes to final year students for open voting and the highest voted option is considered as the anticipated Senior Class Gift for that year. The Graduating Class Gift 2015 was campus benches among other exciting options that included a Water Fountain, an Art Gallery, a Grand Pillar Clock. These campus benches would serve as a memorial of the graduating batch for years to come.<br>
